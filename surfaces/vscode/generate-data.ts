import { parseWord } from "../../src/lint.ts";
import { ENVIRONMENT_SCOPES, INTERACTION_SCOPES, REGISTRY, RELATIONAL_SCOPES, SCALES, STATE_SCOPES } from "../../src/registry.ts";
